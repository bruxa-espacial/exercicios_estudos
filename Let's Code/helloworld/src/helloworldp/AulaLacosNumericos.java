package helloworldp;

public class AulaLacosNumericos {

	public static void main(String[] args) {
		// LAÇOS NUMÉRICOS (COM CONTADOR)
		
		// exibir números de 1 à 10
		for (int i = 1; i <= 10; i++) {
			for (int j = 1; j <= 10; j++) {
				System.out.println(i + " x " + j + " = " + i * j);
			}
			System.out.println("----------");
		}
	}

}
